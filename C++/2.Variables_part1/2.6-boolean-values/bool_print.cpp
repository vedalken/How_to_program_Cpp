// Print boolean values
//
#include <iostream>

int main()
{
		using namespace std;
		
		bool value1 = true;
		bool value2 = false;
		cout << "Value1: " << value1 << endl;
		cout << "Value2: " << value2 << endl;

		cout << "Not Value1: " << !value1 << endl;
		cout << "Not Value2: " << !value2 << endl;

		return 0;
}
